package calculation;

public class LengthCalculation {

    public static double calculateFeetToMeter(double feet){
        return feet / 3.2808;
    }

    public static double calculateFeetToInches(double feet){
        return feet * 12;
    }

    public static double calculateFeetToCm(double feet){
        return feet / 0.032808;
    }

    public static double calculateFeetToYards(double feet){
        return feet * 0.33333;
    }

    public static double calculateFeetToKm(double feet){
        return feet / 3280.8;
    }

    public static double calculateFeetToMiles(double feet){
        return feet * 0.00018939;
    }

}
